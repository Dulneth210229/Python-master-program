# lesson 01: variable in python

student_name = "John Doe"
student_age = 20
student_course = "Software Engineering"
student_year = 4

print("Student Name:", student_name)
print("Student Age:", student_age)
print("Student Course:", student_course)
print("Student Year:", student_year)

# check variable type
print("Type of the student name variable:", type(student_name))
print("Type of the student age variable:", type(student_age))
print("Type of the student course variable:", type(student_course))
print("Type of the student year variable:", type(student_year))

